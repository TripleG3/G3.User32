namespace G3.User32.WindowsHook.Mouse;

/// <summary>
/// The direction of the wheel movement if used.
/// </summary>
public enum MouseWheelDirection
{
    /// <summary>
    /// No movement.
    /// </summary>
    None,

    /// <summary>
    /// Up, away from the user.
    /// </summary>
    Up,

    /// <summary>
    /// Down, towards the user.
    /// </summary>
    Down
}